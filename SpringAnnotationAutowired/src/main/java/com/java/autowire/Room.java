package com.java.autowire;

public class Room {

	private String things;
	private int count;
	private String name;
	public String getThings() {
		return things;
	}
	public void setThings(String things) {
		this.things = things;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
